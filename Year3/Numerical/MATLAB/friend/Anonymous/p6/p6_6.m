% Q23*c2 + Q13*c1 + 500 = Q33*c3

syms c1 c2 c3
eqns = { ...
    500 + 30*c2 == 40*c1 + 90*c1, ...
    90*c1 == 60*c2 + 30*c2, ...
    60*c2 + 40*c1 + 500 == 120*c3, ...
    };
